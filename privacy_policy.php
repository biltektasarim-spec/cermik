    <h2 style="color: #0088cc; margin-bottom: 20px;">Gizlilik Politikası</h2>
    <p>ROTAREHBER olarak kişisel verilerinizin güvenliği hususuna azami hassasiyet göstermekteyiz. Bu bilinçle, platformumuzu kullanan kişilerin her türlü kişisel verilerinin 6698 sayılı Kişisel Verilerin Korunması Kanunu’na (“KVKK”) uygun olarak işlenerek, muhafaza edilmesine büyük önem vermekteyiz.</p>
    
    <h3 style="margin-top: 20px;">1. Veri Sorumlusu</h3>
    <p>KVKK uyarınca, veri sorumlusu sıfatıyla ROTAREHBER, kişisel verilerinizi aşağıda açıklanan amaçlar kapsamında; hukuka ve dürüstlük kurallarına uygun, belirli, açık ve meşru amaçlar için işleyebilecektir.</p>
    
    <h3 style="margin-top: 20px;">2. İşlenen Kişisel Veriler</h3>
    <p>Platformumuz üzerinden sunulan hizmetlerden faydalanmanız sırasında ad, soyad, e-posta, telefon numarası ve konum bilgileriniz gibi veriler işlenebilmektedir.</p>
    
    <h3 style="margin-top: 20px;">3. Kişisel Verilerin İşlenme Amacı</h3>
    <p>Kişisel verileriniz; hizmetlerimizin sunulması, kullanıcı deneyiminin iyileştirilmesi, iletişim faaliyetlerinin yürütülmesi ve yasal yükümlülüklerin yerine getirilmesi amaçlarıyla işlenmektedir.</p>
    
    <h3 style="margin-top: 20px;">4. Çerezler (Cookies)</h3>
    <p>Platformumuzda kullanıcı deneyimini artırmak amacıyla çerezler kullanılmaktadır. Tarayıcı ayarlarınızdan çerez tercihlerinizi yönetebilirsiniz.</p>
    
    <h3 style="margin-top: 20px;">5. Veri Güvenliği</h3>
    <p>Verileriniz yetkisiz erişime, kayba veya ifşaya karşı korumak için gerekli teknik ve idari tedbirler alınmaktadır.</p>
    
